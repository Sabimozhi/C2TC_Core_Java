package com.cg.day1;

public class Primitivedt {

	public static void main(String[] args) {
		// Boolean data type
		boolean a =true;
		System.out.println(a);
		// byte data type
		byte b=127;
		System.out.println(b);
		// short data type
		short c=-230;
		System.out.println(c);
		//int data type
		int d=100;
		System.out.println(d);
		// long data type
		long e=356766523l;
		System.out.println(e);
		//double data type
		double f=4676443.65d;
		System.out.println(f);
		//float data type
		float g=366.7f;
		System.out.println(g);
		//char data type
		char h='c';
		System.out.println(h);
	}
}
